    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try {
            // TODO add your handling code here:
            
            if(rating!=null){
                Rating r = lawyer.getRatings().getEntry(givenBy.getKey());
                r.setFeedBack(feedBackTxt.getText());
                r.setValue(finalRate);
            }
            else{
                rating = new Rating(feedBackTxt.getText(), finalRate, givenBy);
                lawyer.getRatings().addNew(rating);
            }
            this.setVisible(false);
            this.dispose();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_saveBtnActionPerformed

