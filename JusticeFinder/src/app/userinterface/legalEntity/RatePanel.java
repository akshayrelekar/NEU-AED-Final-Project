    public RatePanel(Lawyer lawyer,LegalEntity givenBy, Frame owner, boolean modal,boolean readonly) {
        super(owner, modal);
        initComponents();
        
        starList= new ArrayList<>();
        for(int count =1;count <= 5;count++){
            JLabel label = new JLabel();
            label.setPreferredSize(new Dimension(30,30));
            label.setToolTipText(count+"");
            if(!readonly){
            label.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseClicked(MouseEvent e) {
                    super.mouseClicked(e); //To change body of generated methods, choose Tools | Templates.
                    JLabel label = (JLabel)e.getSource();
                    int r = Integer.parseInt(label.getToolTipText());
                    updateRating(r);
                    finalRate = r;
                }
                
            });
            }
            rateGrid.add(label);
            starList.add(label);
            feedBackTxt.setEnabled(!readonly);
            saveBtn.setVisible(!readonly);
            jLabel1.setVisible(!readonly);
            nameTxt.setVisible(!readonly);
        }
        
        this.lawyer = lawyer;
        this.givenBy = givenBy;
        if(lawyer.getRatings().contains(givenBy.getKey())){
            rating = lawyer.getRatings().getEntry(givenBy.getKey());
        }
        updateRating(rating!=null?rating.getValue():0);
        nameTxt.setText(lawyer.toString());
        feedBackTxt.setText(rating!=null?rating.getFeedBack():"");
        finalRate = rating!=null?rating.getValue():0;
    }
    
    private void updateRating(int rate){
        try {
            int count = 0;
            for(JLabel l: starList){
                if(count<rate){
                    CommonUtils.initPicPanel("src/app/images/starFilled.png", l);
                }
                else{
                    CommonUtils.initPicPanel("src/app/images/star.png", l);
                }
                count++;
            }
        } catch (IOException ex) {
            ex.printStackTrace();
        }
        
    }

